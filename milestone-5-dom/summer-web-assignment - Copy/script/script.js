document.getElementById('card-1').addEventListener('click', function () {
   //get name text
   const items = itemCardText('item-1');
   //set name
   const setName = document.getElementById('set-name');
   const p = document.createElement('p');
   p.innerText = items;
   //set append child
   setName.appendChild(p);
   //card 1 taka
   const card1Taka = cardAndCartInnerText('card-1-taka');
   //total price
   const totalPrices = document.getElementById('total-price');
   const totalPrice = cardAndCartInnerText('total-price');
   //calculation
   const totalTakaCalculation = card1Taka + totalPrice;
   totalPrices.innerText = totalTakaCalculation;
   //apply button
   const applyButtons = enableButton('total-price', 'apply-button');
   //make purchase button
   const btnMakePurchase = enableButton2('total-price', 'btn-make-purchase');
   //discount text
   const discountTk = cardAndCartInnerText('discount');
   //total end
   const totalEnds = document.getElementById('total-end');
   const totalEnd = cardAndCartInnerText('total-end');
   //total end calculation
   const totalEndCalculation = discountTk + totalTakaCalculation;
   totalEnds.innerText = totalEndCalculation;

});
